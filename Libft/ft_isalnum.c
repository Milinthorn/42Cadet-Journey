#include "libft.h"

int	ft_isalnum(int ar)
{
	if ((ar >= 'A' && ar <= 'Z') || (ar >= 'a' && ar <= 'z')
		|| (ar >= 0 && ar <= 9))
		return (1);
	else
		return (0);
}
